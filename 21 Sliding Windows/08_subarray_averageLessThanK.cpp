int numOfSubarrays(vector<int> &arr, int k, int threshold)
{
    int n = arr.size();
    int i = 1;
    int j = k;
    int avj = 0;
    int count = 0;
    for (int i = 0; i < k; i++)
    {
        avj += arr[i];
    }
   
    if (avj/k >= threshold)
    {
        count++;
    }
    while (j < n)
    {
        
        avj -= arr[i - 1];
        avj += arr[j];
      
        if (avj/k>= threshold)
            count++;
        i++;
        j++;
    }
    return count;
}
